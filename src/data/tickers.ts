const BY_SYMBOL: Map<string, Ticker> = new Map(
  TICKERS.map((t) => [t.symbol, t]),
);

/** Look up a ticker by symbol; returns undefined if unknown. */
export function getTicker(symbol: string): Ticker | undefined {
  return BY_SYMBOL.get(symbol.toUpperCase());
}

/** Look up a ticker by symbol; throws if unknown (use where existence is guaranteed). */
export function requireTicker(symbol: string): Ticker {
  const t = BY_SYMBOL.get(symbol.toUpperCase());
  if (!t) throw new Error(`Unknown ticker: ${symbol}`);
  return t;
}

/** Count of the 18 comparable data fields that are non-null for a ticker. */
export function completenessRatio(t: Ticker): number {
  const fields: (keyof Ticker)[] = [
    "marketCapB",
    "price",
    "peRatio",
    "dividendYieldPct",
    "revenueTtmB",
    "netIncomeTtmB",
    "grossMarginPct",
    "netMarginPct",
    "revenueGrowth5yCagrPct",
    "return1yPct",
    "return5yPct",
    "employees",
    "founded",
  ];
  const nonNull = fields.filter((f) => t[f] !== null && t[f] !== undefined).length;
  return nonNull / fields.length;
}
